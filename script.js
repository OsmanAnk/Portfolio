if (contactForm) {
    const fields = [...contactForm.querySelectorAll(".form-field")];
    const requiredInputs = [...contactForm.querySelectorAll('[name="name"], [name="email"], [name="message"]')];
    const privacyBox = contactForm.querySelector("#accept");
    const privacyWrapper = contactForm.querySelector(".check-privacy");
    const submitButton = contactForm.querySelector('button[type="submit"]');

    function isFormReady() {
        return requiredInputs.length === Object.keys(validators).length &&
            requiredInputs.every(isInputValid) &&
            privacyBox.checked;
    }

    /**
     * @param {boolean} isReady
     */
    function setSubmitButtonState(isReady) {
        submitButton.classList.toggle("is-disabled", !isReady);
        submitButton.setAttribute("aria-disabled", String(!isReady));
        submitButton.disabled = !isReady;
    }

    function clearPrivacyError() {
        if (privacyBox.checked) {
            privacyWrapper.classList.remove("is-error");
        }
    }

    function updateSubmitButton() {
        setSubmitButtonState(isFormReady());
        clearPrivacyError();
    }

    /**
     * @param {HTMLElement} field
     */
    function handleFieldBlur(field) {
        validateField(field);
        updateSubmitButton();
    }

    /**
     * @param {HTMLElement} field
     * @returns {boolean}
     */
    function shouldLiveValidate(field) {
        return field.classList.contains("is-error") ||
            field.classList.contains("is-valid");
    }

    /**
     * @param {HTMLElement} field
     */
    function handleFieldInput(field) {
        if (shouldLiveValidate(field)) {
            validateField(field);
        }

        updateSubmitButton();
    }

    /**
     * @param {HTMLElement} field
     */
    function bindFieldEvents(field) {
        const input = getFieldInput(field);

        input.addEventListener("blur", () => handleFieldBlur(field));
        input.addEventListener("input", () => handleFieldInput(field));
    }

    function resetContactForm() {
        contactForm.reset();
        fields.forEach((field) => field.classList.remove("is-valid", "is-error"));
        updateSubmitButton();
    }

    /**
     * @returns {Promise<boolean>}
     */
    async function sendContactForm() {
        const response = await fetch("contact_form_mail.php", {
            method: "POST",
            headers: {
                "Content-Type": "application/json",
            },
            body: JSON.stringify({
                name: contactForm.name.value,
                email: contactForm.email.value,
                message: contactForm.message.value,
            }),
        });

        const result = await response.json();
        return response.ok && result.success;
    }

    /**
     * @param {SubmitEvent} event
     */
    async function handleContactSubmit(event) {
        event.preventDefault();
        const isFormValid = fields.every(validateField);
        const isPrivacyAccepted = privacyBox.checked;
        privacyWrapper.classList.toggle("is-error", !isPrivacyAccepted);

        if (isFormValid && isPrivacyAccepted) {
            submitButton.disabled = true;

            try {
                const wasSent = await sendContactForm();

                if (wasSent) {
                    resetContactForm();
                }
            } catch (error) {
                console.error("Contact form could not be sent:", error);
            } finally {
                updateSubmitButton();
            }
        }
    }

    fields.forEach(bindFieldEvents);
    privacyBox.addEventListener("change", updateSubmitButton);
    contactForm.addEventListener("submit", handleContactSubmit);
    updateSubmitButton();
}
