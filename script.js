let currentLanguage = localStorage.getItem("language") || "en";

/**
 * @param {string} key
 * @returns {string}
 */
function getTranslation(key) {
    return translations[currentLanguage][key] || translations.en[key] || key;
}

/**
 * @param {string} language
 */
function setLanguage(language) {
    setCurrentLanguage(language);
    translateTextElements();
    translateHtmlElements();
    translatePlaceholderElements();
    updateLanguageButtons();
}

/**
 * @param {string} language
 */
function setCurrentLanguage(language) {
    currentLanguage = translations[language] ? language : "en";
    localStorage.setItem("language", currentLanguage);
    document.documentElement.lang = currentLanguage;
}

function translateTextElements() {
    document.querySelectorAll("[data-i18n]").forEach((element) => {
        translateTextElement(element);
    });
}

/**
 * @param {HTMLElement} element
 */
function translateTextElement(element) {
    element.textContent = getTranslation(element.dataset.i18n);

    if (element.hasAttribute("data-label")) {
        element.setAttribute("data-label", element.textContent);
    }
}

function translateHtmlElements() {
    document.querySelectorAll("[data-i18n-html]").forEach((element) => {
        element.innerHTML = getTranslation(element.dataset.i18nHtml);
    });
}

function translatePlaceholderElements() {
    document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
        element.placeholder = getTranslation(element.dataset.i18nPlaceholder);
    });
}

function updateLanguageButtons() {
    document.querySelectorAll(".language-button").forEach((button) => {
        updateLanguageButton(button);
    });
}

/**
 * @param {HTMLButtonElement} button
 */
function updateLanguageButton(button) {
    const isActive = button.dataset.language === currentLanguage;

    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
}

function getBurgerMenuElements() {
    const menu = document.getElementById("navbar");
    const burgerButton = document.getElementById("burger-menu");

    return { menu, burgerButton };
}

/**
 * @param {boolean} isOpen
 */
function setBurgerMenuState(isOpen) {
    const { menu, burgerButton } = getBurgerMenuElements();

    if (!menu || !burgerButton) {
        return;
    }

    menu.classList.toggle("navbar-open", isOpen);
    burgerButton.classList.toggle("menu-open", isOpen);
    burgerButton.setAttribute("aria-expanded", String(isOpen));
    document.body.classList.toggle("menu-is-open", isOpen);
}

function openBurgerMenu() {
    setBurgerMenuState(true);
}

function closeBurgerMenu() {
    setBurgerMenuState(false);
}

document.querySelectorAll("#navbar a").forEach((link) => {
    link.addEventListener("click", closeBurgerMenu);
});

const contactForm = document.getElementById("contact-form");

const validators = {
    /**
     * @param {string} value
     * @returns {boolean}
     */
    name: (value) => value.trim() !== "",
    /**
     * @param {string} value
     * @returns {boolean}
     */
    email: (value) => /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(value.trim()),
    /**
     * @param {string} value
     * @returns {boolean}
     */
    message: (value) => value.trim() !== "",
};

const messageKeys = {
    name: "nameRequired",
    email: "emailRequired",
    message: "messageRequired",
};

/**
 * @param {HTMLElement} field
 * @returns {HTMLInputElement|HTMLTextAreaElement|null}
 */
function getFieldInput(field) {
    return field.querySelector("input, textarea");
}

/**
 * @param {HTMLElement} field
 * @returns {HTMLElement|null}
 */
function getFieldMessage(field) {
    return field.querySelector(".field-message");
}

/**
 * @param {HTMLInputElement|HTMLTextAreaElement} input
 * @returns {boolean}
 */
function isInputValid(input) {
    const validator = validators[input.name];
    return Boolean(validator && validator(input.value));
}

/**
 * @param {HTMLElement} field
 * @param {boolean} isValid
 */
function setFieldState(field, isValid) {
    field.classList.toggle("is-valid", isValid);
    field.classList.toggle("is-error", !isValid);
}

/**
 * @param {HTMLInputElement|HTMLTextAreaElement} input
 * @param {HTMLElement} message
 * @param {boolean} isValid
 */
function setFieldMessage(input, message, isValid) {
    if (input.name === "email" && input.value.trim() !== "" && !isValid) {
        message.textContent = getTranslation("emailInvalid");
    } else {
        message.textContent = getTranslation(messageKeys[input.name]);
    }
}

/**
 * @param {HTMLElement} field
 * @returns {boolean}
 */
function validateField(field) {
    const input = getFieldInput(field);
    const message = getFieldMessage(field);
    const isValid = isInputValid(input);

    setFieldMessage(input, message, isValid);
    setFieldState(field, isValid);
    return isValid;
}

